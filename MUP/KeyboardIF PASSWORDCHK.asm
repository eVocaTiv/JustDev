.model tiny
.DATA
fname1 db 'xc.txt' , 0
fname2 db 'file1.txt' , 0
dat1 db "DEWAN     "
dat2 db "KUNAL"
STR1 DB 'Please enter your username$'
MYSTR DB 'PASSWORD'
MAX1 DB 32
ACT1 DB ?
INP1 DB 32 DUP(0)
NOSTR DB 'NOPE!$'
YESSTR DB 'YUP!$'

handle dw ?
.CODE
.STARTUP

	;DISPLAY NEXT LINE 
	
	MOV DL , 0DH
	MOV AH , 02H
	INT 21H
	MOV DL , 0AH
	MOV AH , 02H
	INT 21H
	

	
	;OUTPUT STR1
	LEA DX , STR1
	MOV AH , 09H
	INT 21H
	
	;DISPLAY NEXT LINE 
	
	MOV DL , 0DH
	MOV AH , 02H
	INT 21H
	MOV DL , 0AH
	MOV AH , 02H
	INT 21H
	
	;INPUT A STRING FROM USER.
	LEA DX , MAX1
	MOV AH , 0AH
	INT 21H
	
	
	;DISPLAY NEXT LINE 
	
	MOV DL , 0DH
	MOV AH , 02H
	INT 21H
	MOV DL , 0AH
	MOV AH , 02H
	INT 21H
	
	;COMPARE MYSTR AND INP1 
	;IF DIFFERENT THEN DISPLAY NO , ELSE DISPLAY YES.
	
	LEA DI ,  INP1
	LEA SI ,  MYSTR
	
	MOV CL , ACT1
	CMP CL , 08H
	JNZ DISPNO
	
COMPARE:

	MOV AL , [DI]
	MOV BL , [SI]
	CMP AL , BL
	;IF CHARACTERS DIFFERENT THEN DISPLAY NO.
	JNZ DISPNO
	;ELSE GO AHEAD.
	INC SI
	INC DI
	DEC CL
	JNZ COMPARE	
	
DISPYES: ;OUTPUT YES
	LEA DX , YESSTR
	MOV AH , 09H
	INT 21H
	JMP FIN
	
	

	
DISPNO : 	;OUTPUT NO 

	LEA DX , NOSTR
	MOV AH , 09H
	INT 21H
	
FIN:	
		
.EXIT
END
