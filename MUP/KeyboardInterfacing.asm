.model tiny
.DATA
fname1 db 'xc.txt' , 0
fname2 db 'file1.txt' , 0
dat1 db "DEWAN     "
dat2 db "KUNAL"
STR1 DB 'HELLOIMKUNAL$'

MAX1 DB 32
ACT1 DB ?
INP1 DB 32 DUP(0)

handle dw ?
.CODE
.STARTUP
	;INPUT CHARACTER WITH ECHO
	MOV AH , 01H
	INT 21H
	
	;DISPLAY NEXT LINE 
	
	MOV DL , 0DH
	MOV AH , 02H
	INT 21H
	MOV DL , 0AH
	MOV AH , 02H
	INT 21H
	
	;DISPLAY CHARACTER A
	
	MOV DL , 'A'
	MOV AH , 02H
	INT 21H
	
	;DISPLAY NEXT LINE 
	
	MOV DL , 0DH
	MOV AH , 02H
	INT 21H
	MOV DL , 0AH
	MOV AH , 02H
	INT 21H
	
	;OUTPUT STR1
	LEA DX , STR1
	MOV AH , 09H
	INT 21H
	
	;DISPLAY NEXT LINE 
	
	MOV DL , 0DH
	MOV AH , 02H
	INT 21H
	MOV DL , 0AH
	MOV AH , 02H
	INT 21H
	
	;INPUT A STRING FROM USER.
	LEA DX , MAX1
	MOV AH , 0AH
	INT 21H
	
	
	;DISPLAY NEXT LINE 
	
	MOV DL , 0DH
	MOV AH , 02H
	INT 21H
	MOV DL , 0AH
	MOV AH , 02H
	INT 21H
	
	;DISPLAY THE STRING ENTERED BY THE USER.
	LEA SI , INP1 ;SI POINTS TO START OF STRING.
	MOV CL , ACT1 ;CL HAS TOTAL CHARACTERS STORED.
	
GO:	INC SI 
	DEC CL
	JNZ GO
	
	;NOW SI POINTS TO LAST CHARACTER OF THE STRING.
	;ADD $ AT LAST.
	INC SI
	MOV AL , '$'
	MOV [SI] , AL
	
	;DISPLAY THE STRING.
	LEA DX , INP1
	MOV AH , 09H
	INT 21H
	
	
	
	

.EXIT
END
